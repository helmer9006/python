def suma(num1, num2):
	resultado = num1+num2
	return resultado

def resta(num1, num2):
	resultado = num1-num2
	return resultado

def multiplicacion(num1, num2):
	resultado = num1*num2
	return resultado

def division(num1, num2):
	resultado = num1/num2
	return resultado

print (suma(5,7))
print (suma(71,25))
print (multiplicacion(5,5))
print (division(25,5))